package task11.package1;

public class MathTest {
    public static void main(String[] args) {
        System.out.println("获取两个整数中最大值的结果是：" + Math.max(12,3));
        System.out.println("获取两个整数中最小值的结果是：" + Math.min(12,3));
        System.out.println("获取数的幂次方：" + Math.pow(2,3));
        System.out.println("获取绝对值的结果是 ：" + Math.abs(-5));
        System.out.println("进行四舍五入的结果是：" + Math.round(3.444444));
        System.out.println("进行四舍五入的结果是：" + Math.round(3.544444));
        System.out.println("该整数的平方根是：" + Math.sqrt(17));
        System.out.println("生成的随机数是：" + Math.random());  //[0,1)
        System.out.println("进行四舍五入的结果是：" + Math.round(-3.544444));
        System.out.println("进行四舍五入的结果是：" + Math.round(-3.44444));


    }

}
